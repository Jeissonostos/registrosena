
package model;

public class aprendiz {
    int id;
    int docua,teleapre;
    String nombrea,apellidoa,correoa;

    public aprendiz() {
    }
    
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDocua() {
        return docua;
    }

    public void setDocua(int docua) {
        this.docua = docua;
    }

    public int getTeleapre() {
        return teleapre;
    }

    public void setTeleapre(int teleapre) {
        this.teleapre = teleapre;
    }

    public String getNombrea() {
        return nombrea;
    }

    public void setNombrea(String nombrea) {
        this.nombrea = nombrea;
    }

    public String getApellidoa() {
        return apellidoa;
    }

    public void setApellidoa(String apellidoa) {
        this.apellidoa = apellidoa;
    }

    public String getCorreoa() {
        return correoa;
    }

    public void setCorreoa(String correoa) {
        this.correoa = correoa;
    }

    
}
